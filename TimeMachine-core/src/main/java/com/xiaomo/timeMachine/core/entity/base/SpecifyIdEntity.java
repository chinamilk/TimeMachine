@MappedSuperclass
public class SpecifyIdEntity {

    @Id
    protected long id;

    @Version
    private long version;


    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getVersion() {
        return version;
    }

    public void setVersion(long version) {
        this.version = version;
    }
}
